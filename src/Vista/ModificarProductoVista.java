    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
         Producto p=new Producto();
         ProductoDataBase producto=new ProductoDataBase();

       try{
       p= producto.buscarProductoPorNombre(nombre.getText());
           if(p.getNombreProducto().equalsIgnoreCase(nombre.getText()) ){
                nuevoNombre.setText(p.getNombreProducto());
                nuevaDescripcion.setText(p.getDescripcion());
                nuevoPrecio.setText(p.getPrecioActual()+"");
                nuevoStock.setText(p.getStock()+"");
                if(p.isEstado()){
                   comboNuevoEstado.setSelectedItem("Activo");
                }else{comboNuevoEstado.setSelectedItem("Inactivo");}
                
        
           
           
    }
       }catch(NullPointerException e){
            JOptionPane.showMessageDialog(this,"Todos los campos deben compterase");
       }
    }//GEN-LAST:event_jButton4ActionPerformed


