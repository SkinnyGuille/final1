    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
         Producto p=new Producto();
         ProductoDataBase producto=new ProductoDataBase();
         try{
             p.setNombreProducto(nuevoNombre.getText());
             p.setDescripcion(nuevaDescripcion.getText());
             p.setPrecioActual(Double.parseDouble(nuevoPrecio.getText()));
             p.setStock(Integer.parseInt(nuevoStock.getText()));
             if(comboNuevoEstado.equals("Activo")){
                 p.setEstado(true);
             }else p.setEstado(false);
             producto.modificarProducto(p, nombre.getText());
         }catch(NullPointerException e){
             JOptionPane.showMessageDialog(this, "Debe completar todos los campos");
         }catch(NumberFormatException e){
             JOptionPane.showMessageDialog(this, "Errror al ingresar los datos numericos");
         }
    }//GEN-LAST:event_modificarActionPerformed

